<h2 style="color:#333;">About MVSR Engineering College</h2>

<p style="font-size:17px; color:#444; line-height:1.7;">
Maturi Venkata Subba Rao (MVSR) Engineering College, established in 1981 and 
located in Nadergul, Hyderabad, is one of the renowned engineering institutions in Telangana.
The college is affiliated to <b>Osmania University</b> and approved by <b>AICTE</b>.
</p>

<p style="font-size:17px; color:#444; line-height:1.7;">
MVSR offers a wide range of UG and PG programs across various engineering disciplines
including <b>CSE, ECE, EEE, Mechanical, Civil, IT, Automobile Engineering</b> and more.
The institution is known for its strong academic foundation, experienced faculty, 
modern infrastructure, and excellent placement record.
</p>

<p style="font-size:17px; color:#444; line-height:1.7;">
The campus encourages research, innovation, and holistic development of students, 
making it one of the top preferred colleges for engineering aspirants in Telangana.
